`timescale 1ns/1ps

module tb_soc_top;

////////////////////////////////////////////////////////////
// CLOCK & RESET
////////////////////////////////////////////////////////////
reg clk;
reg resetn;

always #5 clk = ~clk;

initial begin
    clk = 0;
    resetn = 0;
    #20;
    resetn = 1;
end

////////////////////////////////////////////////////////////
// UART SIGNALS
////////////////////////////////////////////////////////////
reg  uart_rx;
wire uart_tx;

////////////////////////////////////////////////////////////
// USB SIGNALS
////////////////////////////////////////////////////////////
reg  usb_rx_j;
reg  usb_rx_se0;
wire usb_tx_en;
wire usb_tx_j;
wire usb_tx_se0;

////////////////////////////////////////////////////////////
// DUT
////////////////////////////////////////////////////////////
soc_top dut (
    .clk(clk),
    .resetn(resetn),

    .uart_rx(uart_rx),
    .uart_tx(uart_tx),

    .usb_rx_j(usb_rx_j),
    .usb_rx_se0(usb_rx_se0),
    .usb_tx_en(usb_tx_en),
    .usb_tx_j(usb_tx_j),
    .usb_tx_se0(usb_tx_se0)
);

////////////////////////////////////////////////////////////
// STIMULUS
////////////////////////////////////////////////////////////
initial begin

    // init inputs
    uart_rx    = 1;
    usb_rx_j   = 0;
    usb_rx_se0 = 0;

    #50;

    ////////////////////////////////////////////////////////
    // UART RX SIMULATION (optional activity)
    ////////////////////////////////////////////////////////
    repeat (5) begin
        #20 uart_rx = ~uart_rx;
    end

    ////////////////////////////////////////////////////////
    // USB LINE ACTIVITY
    ////////////////////////////////////////////////////////
    #50;
    usb_rx_j   = 1;
    usb_rx_se0 = 0;

    #50;
    usb_rx_j   = 0;
    usb_rx_se0 = 1;

    #50;
    usb_rx_j   = 0;
    usb_rx_se0 = 0;

    ////////////////////////////////////////////////////////
    // RUN SYSTEM (AHB MASTER DRIVES TRANSACTIONS)
    ////////////////////////////////////////////////////////
    #1000;

    $finish;
end

////////////////////////////////////////////////////////////
// WAVEFORM DUMP
////////////////////////////////////////////////////////////
initial begin
    $dumpfile("soc_top.vcd");
    $dumpvars(0, tb_soc_top);
end

////////////////////////////////////////////////////////////
// MONITOR KEY SIGNALS
////////////////////////////////////////////////////////////
initial begin
    $monitor("T=%0t | UART_TX=%b | USB_TX_EN=%b | USB_TX_J=%b | USB_TX_SE0=%b",
        $time, uart_tx, usb_tx_en, usb_tx_j, usb_tx_se0);
end

endmodule
